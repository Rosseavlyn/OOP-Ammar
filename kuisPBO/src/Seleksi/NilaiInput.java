public class NilaiInput {
    int NIK;
    String nama;
    int nilaiTulis;
    int nilaiCoding;
    int nilaiWawancara;

    public NilaiInput(int NIK, String nama, int nilaiTulis, int nilaiCoding, int nilaiWawancara) {
        this.NIK = NIK;
        this.nama = nama;
        this.nilaiTulis = nilaiTulis;
        this.nilaiCoding = nilaiCoding;
        this.nilaiWawancara = nilaiWawancara;
    }
}

